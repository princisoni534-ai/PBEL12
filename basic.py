# Variable

a = 10
print(a)
print(type(a))

a = 10.5
print(a)
print(type(a))

a = "Hello"
print(a)
print(type(a))

a = True
print(a)
print(type(a))

a = 1 + 1j
print(a)
print(type(a))

a = (1, 2, 3, 4)
print(a)
print(type(a))

a = [1, 2, 3, 4]
print(a)
print(type(a))

a = {1, 2, 3, 4}
print(a)
print(type(a))

a = {1: "one", 2: "two", 3: "three"}
print(a)
print(type(a))

